"""Convert DMS coordinates to decimal and generate replacement data."""

def dms_to_decimal(dms_str):
    """Convert DMS string like 11°03'25.9\"N to decimal degrees"""
    # Parse format: 11°03'25.9"N
    import re
    match = re.match(r"(\d+)°(\d+)'([\d.]+)\"([NSEW])", dms_str)
    if not match:
        raise ValueError(f"Invalid DMS format: {dms_str}")
    
    degrees = float(match.group(1))
    minutes = float(match.group(2))
    seconds = float(match.group(3))
    direction = match.group(4)
    
    decimal = degrees + minutes/60 + seconds/3600
    
    # Make negative for S or W
    if direction in ('S', 'W'):
        decimal = -decimal
    
    return round(decimal, 6)


